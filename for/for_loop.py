"""
Looping application for reading book with for
"""

total_book = 10
print('Ibu said, "Read your book"')
total_read = 0

print(f"Total already read {total_read}")
for total_read in range(1, total_book + 1):
    print(f"the book {total_read} is already read")

print(f"Total already read {total_read}")
